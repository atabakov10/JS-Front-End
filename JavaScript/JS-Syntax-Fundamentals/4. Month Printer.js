function solve(numMonth) {
    if (numMonth == 1){
        console.log('January');
    }
    else if (numMonth == 2){
        console.log('February');
    }
    else if (numMonth == 3){
        console.log('March');
    }
    else if (numMonth == 4){
        console.log('April');
    }
    else if (numMonth == 5){
        console.log('May');
    }
    else if (numMonth == 6){
        console.log('June');
    }
    else if (numMonth == 7){
        console.log('July');
    }
    else if (numMonth == 8){
        console.log('August');
    }
    else if (numMonth == 9){
        console.log('September');
    }
    else if (numMonth == 10){
        console.log('October');
    }
    else if (numMonth == 11){
        console.log('November');
    }
    else if (numMonth == 12){
        console.log('December');
    }
    else {
        console.log("Error!")
    }
}

